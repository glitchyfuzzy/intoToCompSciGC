# asks user to type in message
message = input("Input message for analysis")

# checks if a, e, i, o, u, found in message
if "a" in message:
    print("Vowel a found in line")
if "e" in message:
    print("Vowel e found in line")
if "i" in message:
    print("Vowel i found in line")
if "o" in message:
    print("Vowel o found in line")
if "u" in message:
    print("Vowel u found in line")
